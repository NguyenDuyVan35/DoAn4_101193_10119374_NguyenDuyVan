package com.example.udhta_enl_app.HocTap.CDDodunghoctap;

public class TvCDDodunghoctap {
    String TvDDHT;
    String PhiemAmTvDDHT;
    String NghiaTvDDHT;
    String LinkAnhTvDDHT;

    public TvCDDodunghoctap(){}

    public TvCDDodunghoctap(String tvDDHT, String phiemAmTvDDHT, String nghiaTvDDHT, String linkAnhTvDDHT) {
        TvDDHT = tvDDHT;
        PhiemAmTvDDHT = phiemAmTvDDHT;
        NghiaTvDDHT = nghiaTvDDHT;
        LinkAnhTvDDHT = linkAnhTvDDHT;
    }

    public String getTvDDHT() {
        return TvDDHT;
    }

    public void setTvDDHT(String tvDDHT) {
        TvDDHT = tvDDHT;
    }

    public String getPhiemAmTvDDHT() {
        return PhiemAmTvDDHT;
    }

    public void setPhiemAmTvDDHT(String phiemAmTvDDHT) {
        PhiemAmTvDDHT = phiemAmTvDDHT;
    }

    public String getNghiaTvDDHT() {
        return NghiaTvDDHT;
    }

    public void setNghiaTvDDHT(String nghiaTvDDHT) {
        NghiaTvDDHT = nghiaTvDDHT;
    }

    public String getLinkAnhTvDDHT() {
        return LinkAnhTvDDHT;
    }

    public void setLinkAnhTvDDHT(String linkAnhTvDDHT) {
        LinkAnhTvDDHT = linkAnhTvDDHT;
    }
}
